export namespace CALC.struct {
  export class Rectangle {
    public x: number;
    public y: number;
    public width: number;
    public height: number;

    consutuctor(x: number, y: number, width: number, height: number) {
      this.x = x;
      this.y = y;
      this.width = width;
      this.height = height;
    }
  }
}
